package loopexesup1_3;

public class LoopExeSup1_3 {

	public static void main(String[] args) {
		System.out.println("Affichage de valeurs factorielles : ");
		int factorielle = 1;
		
		factorielle = factorielle * 2; 		// Calcul de 2!
		System.out.println(factorielle);	// Affichage de 2!
		factorielle = factorielle * 3; 		// Calcul de 3!
		System.out.println(factorielle);	// Affichage de 3!
		factorielle = factorielle * 4; 		// Calcul de 4!
		System.out.println(factorielle);	// Affichage de 4!
		factorielle = factorielle * 5; 		// Calcul de 5!
		System.out.println(factorielle);	// Affichage de 5!
		factorielle = factorielle * 6; 		// Calcul de 6!
		System.out.println(factorielle);	// Affichage de 6!
		
		System.out.println("Fin de l'affichage");
	}

}

